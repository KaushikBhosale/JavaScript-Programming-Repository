let a = 15;
let b = 11;
// Control Statements
if (a > b) {
    console.log("a is greater than b");
} else if (b > a) {
    console.log("b is greater than a");
} else {
    console.log("a = b");
}

switch (a) {
    case 12:
        console.log("The value a is 12");
        break;

    case 15:
        console.log("The value a is 15");
        break;

    default:
        console.log("Switch Block finnished");
        break;
}

console.log("Thats it about Control Statement");